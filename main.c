#include "raylib.h"

int main()
{
    SetConfigFlags(FLAG_WINDOW_RESIZABLE);
    InitWindow(1600, 800, "Game");
    SetTargetFPS(60);

    while (!WindowShouldClose())
    {

        //----------------------------------------------------------------------
        // UPDATE
        //----------------------------------------------------------------------

        //----------------------------------------------------------------------
        // DRAWING
        //----------------------------------------------------------------------
        BeginDrawing();
        ClearBackground(BLACK);
        DrawFPS(10, 10);
        EndDrawing();
    }

    //----------------------------------------------------------------------
    // DEINIT
    //----------------------------------------------------------------------
    CloseWindow();
    return 0;
}